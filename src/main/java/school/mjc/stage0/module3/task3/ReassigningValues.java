public class ReassigningValues {
    public static void main(String[] args) {
        //write code here
        int linkToFirst=1;
        int linkToSecond=10;
        int linkToThird =100;
        System.out.println(linkToFirst);
        System.out.println(linkToSecond);
        System.out.println(linkToThird);

        linkToFirst=15;
        linkToSecond=6;
        linkToThird=4;
        System.out.println(linkToFirst);
        System.out.println(linkToSecond);
        System.out.println(linkToThird);
        linkToFirst=1;
        linkToSecond=10;
        linkToThird=100;
        System.out.println(linkToFirst);
        System.out.println(linkToSecond);
        System.out.println(linkToThird);
    }
}
